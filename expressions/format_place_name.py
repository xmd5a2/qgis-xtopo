    if " питомник" in n.lower(): n = re.sub(" питомник"," пит.", n, flags=re.IGNORECASE)
    if "охотхозяйство" in n.lower(): n = re.sub("охотхозяйство","охот.", n, flags=re.IGNORECASE)
